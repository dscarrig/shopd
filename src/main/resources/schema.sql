-- Create USERS table (no references to ADDRESSES)
CREATE TABLE USERS (
    USER_ID UUID PRIMARY KEY,
    USERNAME VARCHAR(50) NOT NULL UNIQUE,
    PASSWORD VARCHAR(255) NOT NULL,
    EMAIL VARCHAR(255),
    ACCOUNT_TYPE VARCHAR(50) NOT NULL,
    PAYMENT_INFO TEXT,
    DEFAULT_PAYMENT VARCHAR(500),
    PHONE_NUMBER VARCHAR(50)
);

-- Create ADDRESSES table with foreign key to USERS and default flag
CREATE TABLE ADDRESSES (
    ADDRESS_ID UUID PRIMARY KEY,
    FULL_NAME VARCHAR(255) NOT NULL,
    STREET VARCHAR(255) NOT NULL,
    STREET_LINE2 VARCHAR(255),
    CITY VARCHAR(100) NOT NULL,
    STATE VARCHAR(100) NOT NULL,
    ZIP_CODE VARCHAR(100) NOT NULL,
    COUNTRY VARCHAR(100) NOT NULL,
    USER_ID UUID NOT NULL,
    IS_DEFAULT BOOLEAN DEFAULT FALSE,
    FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID)
);

-- Create SHOPD_ITEMS table
CREATE TABLE SHOPD_ITEMS (
    ITEM_ID UUID PRIMARY KEY,
    NAME VARCHAR(255) NOT NULL,
    DESCRIPTION TEXT NOT NULL,
    PRICE DOUBLE PRECISION NOT NULL,
    IMAGE_URL VARCHAR(500),
    CATEGORY VARCHAR(100) NOT NULL,
    AVAILABLE BOOLEAN NOT NULL,
    QUANTITY INTEGER NOT NULL,
    USER_ID UUID NOT NULL,
    FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID)
);

-- Track individual purchase events: one row per purchase.
-- A user can buy the same item multiple times (no unique constraint on user+item).
CREATE TABLE USER_ITEM_PURCHASES (
    PURCHASE_ID UUID PRIMARY KEY,
    USER_ID UUID NOT NULL,
    ITEM_ID UUID NOT NULL,
    QUANTITY INTEGER NOT NULL DEFAULT 1,
    UNIT_PRICE_AT_PURCHASE DOUBLE PRECISION NOT NULL,
    PURCHASED_AT TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID),
    FOREIGN KEY (ITEM_ID) REFERENCES SHOPD_ITEMS(ITEM_ID)
);

CREATE INDEX IF NOT EXISTS IDX_PURCHASES_USER ON USER_ITEM_PURCHASES(USER_ID);
CREATE INDEX IF NOT EXISTS IDX_PURCHASES_ITEM ON USER_ITEM_PURCHASES(ITEM_ID);
CREATE INDEX IF NOT EXISTS IDX_PURCHASES_USER_ITEM ON USER_ITEM_PURCHASES(USER_ID, ITEM_ID);

-- Create PAYMENT_INFO table for tokenized payment methods
CREATE TABLE PAYMENT_INFO (
    PAYMENT_ID VARCHAR(255) PRIMARY KEY,
    USER_ID VARCHAR(255) NOT NULL,
    PAYMENT_TOKEN VARCHAR(500) NOT NULL UNIQUE,
    CARD_HOLDER_NAME VARCHAR(255) NOT NULL,
    LAST_FOUR_DIGITS VARCHAR(4) NOT NULL,
    EXPIRATION_DATE VARCHAR(7) NOT NULL,
    CARD_TYPE VARCHAR(50),
    BILLING_ADDRESS_ID UUID,
    IS_DEFAULT BOOLEAN NOT NULL DEFAULT FALSE,
    FOREIGN KEY (BILLING_ADDRESS_ID) REFERENCES ADDRESSES(ADDRESS_ID)
);

CREATE TABLE ORDERS (
    ORDER_ID UUID PRIMARY KEY,
    USER_ID UUID NOT NULL,
    TOTAL_AMOUNT DOUBLE PRECISION NOT NULL,
    STATUS VARCHAR(50) NOT NULL,
    CREATED_AT TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID)
);

CREATE TABLE ORDER_ITEMS (
    ORDER_ITEM_ID UUID PRIMARY KEY,
    ORDER_ID UUID NOT NULL,
    ITEM_ID UUID NOT NULL,
    QUANTITY INTEGER NOT NULL,
    FOREIGN KEY (ORDER_ID) REFERENCES ORDERS(ORDER_ID),
    FOREIGN KEY (ITEM_ID) REFERENCES SHOPD_ITEMS(ITEM_ID)
);